load "groebner.m"; // don't forget to set N = 5 in groebner.m

// 0 a
// 1 b1
// 2 b2
// 3 p
// 4 z

procedure main()
  h1 := [ < 1.0, [1, 1, 0, 0, 0]> //  1 * a * b1
        , <-1.0, [0, 2, 0, 0, 0]> // -1 * b1^2
        , <-1.0, [0, 0, 2, 0, 0]> // -1 * b2^2
        ];
  h2 := [ < 1.0, [0, 1, 0, 1, 0]> //  1 * b1 * p
        , <-1.0, [0, 0, 0, 2, 0]> // -1 * p^2
        ];
  t :=  [ < 1.0, [1, 0, 0, 1, 1]> //  1 * a * p * z
        , <-1.0, [0, 2, 0, 0, 1]> // -1 * b1^2 * z
        , <-1.0, [0, 0, 2, 0, 1]> // -1 * b2^2 * z
        , <-1.0, [0, 0, 0, 0, 0]> // -1
        ];
  B := [h1, h2, t];
  one := [ <1.0, [0, 0, 0, 0, 0]> ];
  reduce(B, one);
end procedure;

