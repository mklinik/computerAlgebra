load "groebner.m"; // don't forget to set N = 4 in groebner.m

// 0 a
// 1 b1
// 2 b2
// 3 z

procedure main()
  h1 := [ < 1.0, [1, 1, 0, 0]> //  1 * a * b1
        , <-1.0, [0, 2, 0, 0]> // -1 * b1^2
        , <-1.0, [0, 0, 2, 0]> // -1 * b2^2
        ];
  t :=  [ < 1.0, [2, 2, 0, 1]> //  1 * a^2 * b1^2 * z
        , <-1.0, [0, 4, 0, 1]> // -1 * b1^4 * z
        , <-1.0, [0, 2, 2, 1]> // -2 * b1^2 * b2^2 * z
        , <-1.0, [0, 0, 4, 1]> // -1 * b2^4 * z
        , <-1.0, [0, 0, 0, 0]> // -1
        ];
  B := [h1, t];
  one := [ <1.0, [0, 0, 0, 0]> ];
  reduce(B, one);
end procedure;

